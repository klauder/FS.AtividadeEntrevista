        /// <summary>
        /// Verifica se o CPF informado é valido, conforme o cálculo padrão de verificação do dígito verificador de CPF
        /// </summary>
        /// <param name="CPF"></param>
        /// <returns></returns>
        internal bool CPFValido(string CPF)
        {
            int[] multiplicador1 = new int[9] { 10, 9, 8, 7, 6, 5, 4, 3, 2 };
            int[] multiplicador2 = new int[10] { 11, 10, 9, 8, 7, 6, 5, 4, 3, 2 };

            //removemos os pontos e hífen
            CPF = CPF.Trim().Replace(".", "").Replace("-", "");

            //O CPF deve ter 11 digitos após a remoção dos pontos e hífen
            if (CPF.Length != 11)
                return false;

            for (int j = 0; j < 10; j++)
                if (j.ToString().PadLeft(11, char.Parse(j.ToString())) == CPF)
                    return false;

            string auxCpf = CPF.Substring(0, 9);
            int soma = 0;

            for (int i = 0; i < 9; i++)
                soma += int.Parse(auxCpf[i].ToString()) * multiplicador1[i];

            int resto = soma % 11;
            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;

            string digito = resto.ToString();
            auxCpf = auxCpf + digito;
            soma = 0;
            for (int i = 0; i < 10; i++)
                soma += int.Parse(auxCpf[i].ToString()) * multiplicador2[i];

            resto = soma % 11;
            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;

            digito = digito + resto.ToString();

            return CPF.EndsWith(digito);
        }

